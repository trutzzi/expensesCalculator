export default function PieComponent({ dataCompare }: {
    dataCompare: PieData[]
}) {
    const [data, setData] = useState<any>([])
    useEffect(() => {
        if (dataCompare.length) {
            console.log(dataCompare)
            setData({
                labels: dataCompare?.map((item: PieData) => item.tag),
                datasets: [
                    {
                        label: 'RON',
                        data: dataCompare?.map((item: PieData) => item.total),
                        backgroundColor: [
                            'rgba(255, 99, 132, 0.2)',
                            'rgba(54, 162, 235, 0.2)',
                            'rgba(255, 206, 86, 0.2)',
                            'rgba(75, 192, 192, 0.2)',
                            'rgba(153, 102, 255, 0.2)',
                            'rgba(255, 159, 64, 0.2)',
                        ],
                        borderColor: [
                            'rgba(255, 99, 132, 1)',
                            'rgba(54, 162, 235, 1)',
                            'rgba(255, 206, 86, 1)',
                            'rgba(75, 192, 192, 1)',
                            'rgba(153, 102, 255, 1)',
                            'rgba(255, 159, 64, 1)',
                        ],
                        borderWidth: 1,
                    },
                ],
            })
        } else {
            setData([])
        }
    }, [dataCompare]);

    return (
        data.length ? <Pie data={data} /> : <Typography variant='h6'>Select file document for chart</Typography>
    )

}
